const SESSION_KEY = 'univ_session';

const Auth = {
  login: (username, password) => {
    const db = getDB();
    const user = db.users.find(u => u.username === username && u.password === password && u.active);
    if (!user) return null;
    const session = { id: user.id, username: user.username, name: user.name, role: user.role, studentId: user.studentId };
    sessionStorage.setItem(SESSION_KEY, JSON.stringify(session));
    return session;
  },
  logout: () => {
    sessionStorage.removeItem(SESSION_KEY);
    window.location.href = 'index.html';
  },
  getSession: () => {
    const raw = sessionStorage.getItem(SESSION_KEY);
    return raw ? JSON.parse(raw) : null;
  },
  require: (allowedRoles) => {
    const session = Auth.getSession();
    if (!session) { window.location.href = 'index.html'; return null; }
    if (allowedRoles && !allowedRoles.includes(session.role)) {
      window.location.href = 'dashboard.html';
      return null;
    }
    return session;
  },
  is: (role) => {
    const s = Auth.getSession();
    return s && s.role === role;
  },
  hasRole: (...roles) => {
    const s = Auth.getSession();
    return s && roles.includes(s.role);
  }
};

